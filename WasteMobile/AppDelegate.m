- (void)saveContext
{
    NSError *error = nil;
    NSManagedObjectContext *managedObjectContext = self.managedObjectContext;
    if (managedObjectContext != nil) {
        if ([managedObjectContext hasChanges] && ![managedObjectContext save:&error]) {
            NSLog(@"Error while saving %@, %@", error, [error userInfo]);
        }
    }
}


#pragma mark - code table initialization
- (void)initCodeTable{
    NSManagedObjectContext *context = [self managedObjectContext];
    
    NSFetchRequest *fetchRequest = [[NSFetchRequest alloc] init];
    NSEntityDescription *entity = [NSEntityDescription entityForName:@"BorderlineCode"
                                              inManagedObjectContext:context];
    [fetchRequest setEntity:entity];
    NSError *error;
    NSArray *fetchedObjects = [context executeFetchRequest:fetchRequest error:&error];
    
    NSLog(@"Initial code table in core data");
    
    if(error != nil){
        NSLog(@"Error when checking the existence of code table: %@", error);
    }else{
        if([fetchedObjects count] == 0){
            //add the code table there
            [[CodeDAO sharedInstance] initCodeTable:&error];
            if ( error != nil){
                NSLog(@"Error when initializing the code table data: %@", error);
            }
        }
    }
    
    //[self printCodeTable];
}

